//The ternary operator (? :) is a shorthand for if-else, used for concise conditional expressions, while if-else handles multiple statements.
let a = 20;
let result = (a > 10) ? "Greater" : "Smaller";   // Ternary operator  
console.log(result);

let x = 4;
if (x > 10) { result = "Greater"; } 
else { result = "Smaller"; }  // if-else statement