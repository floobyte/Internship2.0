// Question49.  What is the difference between the ternary (? :) operator and if-else statements?  

// Ternary (? :) Operator: A concise, one-line expression used for simple conditional checks. 

let result = (age >= 18) ? "Adult" : "Minor";

// if-else Statement: A more verbose, multi-line conditional structure, better for complex logic and performing multiple actions.

if (age >= 18) {
    result = "Adult";
} else {
    result = "Minor";
}