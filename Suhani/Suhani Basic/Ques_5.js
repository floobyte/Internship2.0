// Question5.  What basic mathematical operations are allowed in JavaScript

// Addition
console.log(5 + 3); 

// Subtraction
console.log(10 - 4); 

// Multiplication
console.log(6 * 7);

// Division
console.log(20 / 4);

// Comparison Operators (<=, >=, ==, !=, ===, !==)
console.log(5 <= 10); 
console.log(10 <= 10); 
console.log(15 <= 10); 

// Boolean conditions
let age = 18;

if (age >= 18) {
    console.log("You are eligible to vote!");
} else {
    console.log("You are not eligible to vote.");
}