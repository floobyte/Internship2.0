
// Question50.  What are arithmetic operators in JavaScript? List some examples.

let sum = 3 + 5;
console.log(sum); //Output = 8

let sub = 9 - 6; 
console.log(sub);  //Output = 3

let mul =  2 * 4;  
console.log(mul);  //Output = 8

let divide = 45 / 5;   
console.log(divide);  //Output = 9

let mod = 6 % 4; 
console.log(mod);   //Output = 2

let expo = 3 ** 3; 
console.log(expo);   //Output = 27